        <p class="content08p">Control of venereal diseases (VD) in Sri Lanka dates back to few hundred years. History records that the first VD clinics were set up in 1886, in Colombo, Kandy and Galle. Even before that the Vagrance ordinance No 4 of 1841, the Contagious Disease Ordinance No 17 of 1867 and the Brothels Ordinance No5 of 1889 were passed in view of controlling venereal diseases. In 1878, Lock Wards were established in Colombo and Kandy to treat women. In 1920, late Sir Winston Churchill visited Ceylon and following this visit control measures were established in a more organised manner and the Ceylon National Council for the control of VD was established. In 1921, a clinic was opened in the Colombo Port for the benefit of sailors. In 1928, Ceylon was a signitory to the Brussels agreement to provide treatment for sailors.Since 1952, measures to control venereal diseases in Ceylon were the responsibility of the Anti VD Campaign. Control of venereal diseases were in existance for a nearly hundred years and in 1949, at the request of the Government of Ceylon, the World Health Organization made available to us the services of a venereal disease consultant, Professor George Leiby. Thereafter under the leadership of Dr S M Laird a venereal disease control team arrived in July 1951 to plan a coordinated programme for Sri Lanka and the Anti Venereal Disease Campaign was established in 1952. Apart from strengthening clinic services, the WHO team recommended certain strtaegies. Since 1952, routine prenatal and pre employment blood screening was introduced. In 1953, another routine examination that of blood testing of all admissions to the Eye Hospital Colombo was commenced. These strategies have helped in controlling venereal diseases in Ceylon.</p>
